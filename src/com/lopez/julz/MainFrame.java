    public double getSumOr(List<PaidBills> ors) {
        try {            
            double sum = 0;
            for (int i=0; i<ors.size(); i++) {
                sum += Double.valueOf(ors.get(i).getNetAmount());
            }
            return sum;
        } catch (Exception e) {
            e.printStackTrace();
            return 0;
        }
    }
